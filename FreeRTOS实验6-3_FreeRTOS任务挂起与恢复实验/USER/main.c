/*
1、实验目的
学习使用 FreeRTOS 的任务挂起和恢复相关 API 函数，包括 vTaskSuspend()、 vTaskResume()
和 xTaskResumeFromISR()。
2、实验设计
本实验设计 4 个任务： start_task、 key_task、 task1_task 和 task2_task，
这四个任务的任务功能如下：
	start_task：用来创建其他 3 个任务。
	key_task	： 按键服务任务，检测按键的按下结果，根据不同的按键结果执行不同的操作。
	task1_task：应用任务 1。
	task2_task: 应用任务 2。

实验需要四个按键， KEY0、 KEY1、 KEY2 和 KEY_UP，这四个按键的功能如下：
	KEY0: 此按键为中断模式，在中断服务函数中恢复任务 2 的运行。
	KEY1: 此按键为输入模式，用于恢复任务 1 的运行。
	KEY2: 此按键为输入模式，用于挂起任务 2 的运行。
	KEY_UP: 此按键为输入模式，用于挂起任务 1 的运行。
*/
#define START_TASK_PRIO 						1 //任务优先级
#define START_STK_SIZE 						256	//任务堆栈大小
TaskHandle_t StartTask_Handler; 			//任务句柄
void start_task(void *pvParameters); 	//任务函数

#define KEY_TASK_PRIO														2
#define KEY_STK_SIZE													128
TaskHandle_t KeyTask_Handler;			
void key_task(void * pvParameters);


#define TASK1_TASK_PRIO                         2
#define TASK1_STK_SIZE                        128
TaskHandle_t Task1Task_Handler;
void task1_task(void * pvParameters);


#define TASK2_TASK_PRIO                         3
#define TASK2_STK_SIZE                        128
TaskHandle_t Task2Task_Handler;
void task2_task(void * pvParameters);


//LCD刷屏时使用的颜色
int lcd_discolor[14]={	WHITE, BLACK, BLUE,  BRED,      
						GRED,  GBLUE, RED,   MAGENTA,       	 
						GREEN, CYAN,  YELLOW,BROWN, 			
						BRRED, GRAY };
    
//●  main()函数
int main(void)
{
	NVIC_PriorityGroupConfig(NVIC_PriorityGroup_4);	//设置系统中断优先级分组 4
	delay_init(168); 																//初始化延时函数
	uart_init(115200); 															//初始化串口
	LED_Init(); 																		//初始化 LED 端口
	KEY_Init();							//初始化按键
	EXTIX_Init();						//初始化外部中断
	LCD_Init();							//初始化LCD
	 
	POINT_COLOR = RED;
	LCD_ShowString(30,10,200,16,16,"ATK STM32F103/F407");	
	LCD_ShowString(30,30,200,16,16,"FreeRTOS Examp 6-1");
	LCD_ShowString(30,50,200,16,16,"Task Creat and Del");
	LCD_ShowString(30,70,200,16,16,"ATOM@ALIENTEK");
	LCD_ShowString(30,90,200,16,16,"2016/11/25");
	
	//创建开始任务
	xTaskCreate((TaskFunction_t )start_task, 		//任务函数
							(const char* )	"start_task", 		//任务名称
							(uint16_t )				START_STK_SIZE, 			//任务堆栈大小
							(void* )					NULL, 								//传递给任务函数的参数
							(UBaseType_t )		START_TASK_PRIO, 			//任务优先级
							(TaskHandle_t*)		&StartTask_Handler);
	vTaskStartScheduler(); //开启任务调度
}
//● 任务函数
//开始任务任务函数
void start_task(void *pvParameters)
{
	taskENTER_CRITICAL(); //进入临界区
  //创建KEY任务
	xTaskCreate((TaskFunction_t )key_task,
							(const char*		)"key_task",
							(uint16_t				)KEY_STK_SIZE,
							(void *					)NULL,
							(UBaseType_t		)KEY_TASK_PRIO,
							(TaskHandle_t*		)&KeyTask_Handler);
	//创建TASK1任务
  xTaskCreate((TaskFunction_t )task1_task,
							(const char *   )"task1_task",
							(uint16_t       )TASK1_STK_SIZE,
							(void *         )NULL,
							(UBaseType_t    )TASK1_TASK_PRIO,
							(TaskHandle_t*  )&Task1Task_Handler);
	//创建TASK2任务
	xTaskCreate((TaskFunction_t )task2_task,
							(const char*    )"task2_task",
							(uint16_t       )TASK2_STK_SIZE,
							(void*          )NULL,
							(UBaseType_t    )TASK2_TASK_PRIO,
							(TaskHandle_t*  )&Task2Task_Handler);
	vTaskDelete(StartTask_Handler); //删除开始任务
	taskEXIT_CRITICAL(); //退出临界区
}

//key任务函数
void key_task(void * pvParameters)
{
	u8 key;
	while(1)
	{
		key=KEY_Scan(0);
		switch(key)
		{
			case WKUP_PRES:
				vTaskSuspend(Task1Task_Handler);
				printf("挂起任务1的运行!\r\n");
				break;
			case KEY1_PRES:
				vTaskResume(Task1Task_Handler);
				printf("恢复任务1的运行!\r\n");
				break;
			case KEY2_PRES:
				vTaskSuspend(Task2Task_Handler);
				printf("挂起任务2的运行!\r\n");
				break;
		}
		vTaskDelay(10);			//延时10ms
	}
}

//task1任务函数
void task1_task(void * pvParameters)
{
    u8 task1_num=0;

    POINT_COLOR = BLACK;
    
    LCD_DrawRectangle(5,110,115,314);    //画一个矩形
    LCD_DrawLine(5, 130, 115, 130);         //画线
    POINT_COLOR =  BLUE;
    LCD_ShowString(6,111,110,16,16,"Task1 Run:000");
    while(1)
    {
        task1_num++;                          //任务执1行次数加1 注意task1_num1加到255的时候会清零！！
        LED0=!LED0;
        printf("任务1已经执行：%d次\r\n",task1_num);
        LCD_Fill(6,131,114,313,lcd_discolor[task1_num%14]);     //填充区域
        LCD_ShowxNum(86,111,task1_num,3,16,0x80);               //显示任务执行次数
        vTaskDelay(1000);                                       //延时1s，也就是1000个时钟节拍
    }
}

//task2任务函数
void task2_task(void * pvParameters)
{
    u8 task2_num=0;
    POINT_COLOR = BLACK;
    
    LCD_DrawRectangle(125,110,234,314);     //画一个矩形
    LCD_DrawLine(125,130,234,130);          //画线
    POINT_COLOR = BLUE;
    LCD_ShowString(126,111,110,16,16,"Task2 Run:000");
    while(1)
    {
        task2_num++;                        //任务2执行次数加1 注意task1_num2加到255时会清零！！
        LED1=!LED1;
        printf("任务2已经执行：%d次\r\n",task2_num);
        LCD_ShowxNum(206,111,task2_num,3,16,0x80);      //显示任务执行次数
        LCD_Fill(126,131,233,313,lcd_discolor[13-task2_num%14]);    //填充区域
        vTaskDelay(1000);                               //延时1s，也就是1000个时钟节拍
    }
}
